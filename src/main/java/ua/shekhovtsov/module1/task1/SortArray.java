package ua.shekhovtsov.module1.task1;

public class SortArray {
    SortArray(int[] array, boolean sortType) {
        if (sortType) {
            arrayAscendingSort(array);
        } else {
            arrayDescendingSort(array);
        }
    }

    private static void arrayAscendingSort(int[] array) {
        for (int i = 0; i < array.length - 1; i++) {
            for (int j = array.length - 1; j > i; j--) {
                if (array[j - 1] > array[j]) {
                    int temp = array[j - 1];
                    array[j - 1] = array[j];
                    array[j] = temp;
                }
            }
        }
    }

    private static void arrayDescendingSort(int[] array) {
        for (int i = 0; i < array.length - 1; i++) {
            for (int j = array.length - 1; j > i; j--) {
                if (array[j - 1] < array[j]) {
                    int temp = array[j - 1];
                    array[j - 1] = array[j];
                    array[j] = temp;
                }
            }
        }
    }
}
